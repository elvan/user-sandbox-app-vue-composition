<template>
  <section class="container">
    <h2>{{ user.name }}</h2>
    <h3>{{ user.age }}</h3>
  </section>
</template>

<script>
import { reactive } from 'vue';

export default {
  setup() {
    const user = reactive({
      name: 'Elvan',
      age: 31,
    });

    setTimeout(function() {
      user.name = 'Hidayat';
      user.age = 32;
    }, 2000);

    return { user: user };
  },
};
</script>

